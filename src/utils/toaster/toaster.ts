import { toast } from "react-toastify";

export const errorHandler = (error: string) => {
  toast.dismiss();
  toast.error(`${error}`, {
    position: "bottom-left",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};

